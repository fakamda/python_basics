#suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5
#multiplicacion y division (* y /)
multiplicacion = 12 * 5
division = 12 / 5
# --> la division nos devuelve un float siempre

#potencia (**)
exponente = 12 ** 5
#division baja (//) --> devuelve entero redondeado hacia abajo
div_baja = 12 // 5
#resto (%)
resto = 12 % 5

#type(dato) nos devuelve el tipo de dato
tipo_de_dato = type(division)
print(tipo_de_dato) #float

print(suma, resta, multiplicacion, division, exponente, div_baja, resto)